int evaluate(const Board& b) {
    int score = 0;
    for (int s = 0; s < 64; s++) {
        int8_t p = b.sq[s];
        if (p == EMPTY) continue;

        int pt  = piece_type(p);
        int pst_sq = (p > 0) ? s : (s ^ 56); // mirror for black
        int val = MAT[pt] + PST[pt][pst_sq];

        score += (p > 0) ? val : -val;
    }
    return score; // positive = good for white
}
